    def process(self):
        err_columns = (col2stat("errhi", self.data_columns()) + col2stat("errlo", self.data_columns()))
        err_df = pd.DataFrame(0, index=self.df.index, columns=err_columns)
        self.df = pd.concat([self.df, err_df], axis=1)
